ANSI_RED = "\033[31m"
ANSI_RESET = "\033[0m"


def color_red(text):
    """Render text in red when stdout is attached to a terminal."""
    if sys.stdout.isatty():
        return f"{ANSI_RED}{text}{ANSI_RESET}"
    return text


def format_running_counts(stats):
    """Return a compact running counter summary for console and file logs."""
    return f"ok={stats['valid']} bad={stats['invalid']} err={stats['errors']}"


def strip_ansi(text):
    """Remove ANSI escape sequences before writing text to a file."""
    return re.sub(r"\x1b\[[0-9;]*m", "", text)


def set_console_log_handle(handle):
    """Register the mirror log handle for console-visible messages."""
    global CONSOLE_LOG_HANDLE
    CONSOLE_LOG_HANDLE = handle


def write_console_log(text):
    """Write a console message to the mirror log file if enabled."""
    if CONSOLE_LOG_HANDLE is None:
        return
    CONSOLE_LOG_HANDLE.write(strip_ansi(text))
    CONSOLE_LOG_HANDLE.flush()


def console_print(*parts, sep=" ", end="\n", color=None):
    """Print to stdout and mirror the same content into the console log file."""
    text = sep.join(str(part) for part in parts)
    rendered = color_red(text) if color == "red" else text
    print(rendered, end=end)
    write_console_log(text + end)


def console_tqdm_write(text, color=None):
    """Emit a tqdm-safe console line and mirror it into the console log file."""
    rendered = color_red(text) if color == "red" else text
    tqdm.write(rendered)
    write_console_log(text + "\n")

