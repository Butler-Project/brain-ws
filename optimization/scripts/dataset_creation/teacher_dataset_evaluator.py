def clean_results():
    """Remove the entire generated artifacts directory configured for the pipeline."""
    console_print("=" * 60)
    console_print("CLEANING ALL GENERATED ARTIFACTS")
    console_print("=" * 60)

    if ARTIFACTS_DIR is None:
        console_print("  ERROR: Could not infer artifacts directory from config.yaml", color="red")
        console_print()
        return

    if ARTIFACTS_DIR.exists():
        shutil.rmtree(ARTIFACTS_DIR)
        console_print(f"  Removed directory tree: {ARTIFACTS_DIR}")
    else:
        console_print(f"  Nothing to clean. Directory does not exist: {ARTIFACTS_DIR}")
    console_print()


