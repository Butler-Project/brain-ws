# CLI
# ============================================================

def main():
    parser = argparse.ArgumentParser(
        description="Generate training dataset using 8B teacher model (JSON→JSON protocol)"
    )
    parser.add_argument(
        "--ollama-url",
        default=OLLAMA_URL,
        help=f"Ollama API URL (default: {OLLAMA_URL})",
    )
    parser.add_argument(
        "--model",
        default=MODEL_NAME,
        help=f"Ollama model name (default: {MODEL_NAME})",
    )
    parser.add_argument(
        "--max-examples", "-n",
        type=int,
        default=CONFIG.get("dataset", {}).get("max_examples"),
        help="Max total examples to process (default: all)",
    )
    parser.add_argument(
        "--category", "-c",
        choices=["all"] + list(CATEGORY_KEYS.keys()),
        action="append",
        default=None,
        help=(
            "Category to process. Can be repeated for multiple. "
            "Choices: all, explicit, implicit, natural_language, invalids. "
            "Default: all"
        ),
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=CONFIG.get("dataset", {}).get("seed", 42),
        help="Random seed (default: 42)",
    )
    parser.add_argument(
        "--clean_results",
        action="store_true",
        help="Remove the configured artifacts directory and exit",
    )

    args = parser.parse_args()

    if args.clean_results:
        clean_results()
        return

    random.seed(args.seed)

    # Resolve categories: None or ["all"] → run everything
    selected_categories = args.category
    if not selected_categories or "all" in selected_categories:
        selected_categories = None  # generate_dataset interprets None as "all"

    if CONSOLE_LOG_PATH is None:
        # Connectivity check
        console_print("Checking Ollama connectivity...")
        test = call_ollama(
            json.dumps({"version": "1.0", "request_type": "from_user", "user_message": "hello"}),
            ollama_url=args.ollama_url,
            model=args.model,
        )
        if test is None:
            console_print(
                f"ERROR: Cannot reach Ollama at {args.ollama_url} with model {args.model}",
                color="red",
            )
            console_print("Make sure the Ollama Docker container is running.", color="red")
            sys.exit(1)
        console_print(f"OK — Ollama responded in {test['total_duration_ms']:.0f} ms\n")

        generate_dataset(
            ollama_url=args.ollama_url,
            model=args.model,
            categories=selected_categories,
            max_examples=args.max_examples,
        )
        return

    LOGS_DIR.mkdir(parents=True, exist_ok=True)
    with open(CONSOLE_LOG_PATH, "w") as console_log:
        set_console_log_handle(console_log)
        try:
            console_print(f"Teacher validation log started at {time.strftime('%Y-%m-%d %H:%M:%S')}")
            console_print(f"Console log file: {CONSOLE_LOG_PATH}")
            console_print()

            # Connectivity check
            console_print("Checking Ollama connectivity...")
            test = call_ollama(
                json.dumps({"version": "1.0", "request_type": "from_user", "user_message": "hello"}),
                ollama_url=args.ollama_url,
                model=args.model,
            )
            if test is None:
                console_print(
                    f"ERROR: Cannot reach Ollama at {args.ollama_url} with model {args.model}",
                    color="red",
                )
                console_print("Make sure the Ollama Docker container is running.", color="red")
                sys.exit(1)
            console_print(f"OK — Ollama responded in {test['total_duration_ms']:.0f} ms\n")

            generate_dataset(
                ollama_url=args.ollama_url,
                model=args.model,
                categories=selected_categories,
                max_examples=args.max_examples,

            )
        finally:
            set_console_log_handle(None)


if __name__ == "__main__":
    main()
