def load_dataset(path):
    """Load entries from a JSONL dataset file."""
    entries = []
    if not path.exists():
        console_print(f"WARNING: Dataset not found: {path}", color="red")
        return entries
    with open(path) as f:
        for line_num, line in enumerate(f, 1):
            line = line.strip()
            if not line:
                continue
            try:
                entries.append(json.loads(line))
            except json.JSONDecodeError as e:
                console_print(f"WARNING: Invalid JSON at {path}:{line_num}: {e}", color="red")
    return entries


