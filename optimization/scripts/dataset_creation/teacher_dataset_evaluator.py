def process_category(category_name, entries, ollama_url, model, output_path, log,
                     pbar_global=None, global_counts=None):
    """Process all entries in one category: send to model, validate, write results."""

    stats = {
        "total": len(entries),
        "valid": 0,
        "invalid": 0,
        "errors": 0,
        "inference_times_ms": [],
    }

    results = []

    log.write(f"\n{'='*80}\n")
    log.write(f"CATEGORY: {category_name} ({len(entries)} entries)\n")
    log.write(f"{'='*80}\n\n")

    pbar = tqdm(
        entries,
        desc=f"  {category_name:<20}",
        unit="prompt",
        position=1,
        leave=True,
    )

    for entry in pbar:
        input_obj = entry["input"]
        expected_output = entry["expected_output"]
        use_case = entry.get("use_case", "?")
        subcategory = entry.get("subcategory", "?")

        # Serialize input to JSON string (this is what the model receives)
        prompt_json = json.dumps(input_obj, ensure_ascii=False)

        # Call teacher model
        t_start = time.time()
        response = call_ollama(prompt_json, ollama_url=ollama_url, model=model)
        wall_time_ms = (time.time() - t_start) * 1000

        if response is None:
            stats["errors"] += 1
            counts = format_running_counts(stats)
            log.write(
                f"[ERROR  ] [{use_case:5s}] [{subcategory:35s}] "
                f"{counts}  "
                f"wall={wall_time_ms:.0f}ms  "
                f"msg=\"{input_obj.get('user_message', '')[:60]}\"\n"
            )
            console_tqdm_write(
                f"  ERROR {counts} [{use_case}] [{subcategory}] "
                f"\"{input_obj.get('user_message', '')[:50]}\" "
                f"→ Ollama connection error",
                color="red",
            )
            results.append({
                **entry,
                "response": None,
                "validation": {"valid": False, "issues": ["Ollama connection error"]},
                "metrics": {"wall_time_ms": wall_time_ms},
            })
            pbar.set_postfix_str(
                f"ok={stats['valid']} bad={stats['invalid']} err={stats['errors']}"
            )
            if pbar_global is not None:
                if global_counts is not None:
                    global_counts["errors"] += 1
                pbar_global.update(1)
                if global_counts is not None:
                    pbar_global.set_postfix_str(
                        f"ok={global_counts['valid']} bad={global_counts['invalid']} "
                        f"err={global_counts['errors']}  [{category_name}]"
                    )
            continue

        # Validate
        validation = validate_response(response["content"], expected_output)

        inference_ms = response["total_duration_ms"]
        stats["inference_times_ms"].append(inference_ms)

        # Build result entry
        result_entry = {
            **entry,
            "response": response["content"],
            "validation": {
                "valid": validation["valid"],
                "is_json": validation["is_json"],
                "result_type": validation["result_type"],
                "command_name": validation["command_name"],
                "landmarks": validation["landmarks"],
                "follow_up": validation["follow_up"],
                "issues": validation["issues"],
            },
            "metrics": {
                "total_duration_ms": inference_ms,
                "wall_time_ms": wall_time_ms,
                "prompt_tokens": response["prompt_tokens"],
                "completion_tokens": response["completion_tokens"],
            },
        }
        results.append(result_entry)

        if validation["valid"]:
            stats["valid"] += 1
        else:
            stats["invalid"] += 1

        counts = format_running_counts(stats)

        # Log
        status = "OK" if validation["valid"] else "INVALID"
        log.write(
            f"[{status:7s}] [{use_case:5s}] [{subcategory:35s}] "
            f"{counts}  "
            f"inference={inference_ms:7.0f}ms  wall={wall_time_ms:7.0f}ms  "
            f"tokens={response['prompt_tokens']}+{response['completion_tokens']}  "
            f"msg=\"{input_obj.get('user_message', '')[:60]}\""
        )
        if validation["issues"]:
            log.write(f"\n         ISSUES: {validation['issues']}")
        log.write("\n")

        # Expected vs actual logging
        exp_cmd = expected_output.get("command")
        exp_cmd_name = exp_cmd["name"] if exp_cmd else "null"
        exp_landmarks = (
            exp_cmd.get("parameters", {}).get("landmarks_to_visit", []) if exp_cmd else []
        )
        exp_follow_up = "confirmation" if expected_output.get("follow_up") else "null"
        act_follow_up = "confirmation" if validation["follow_up"] else "null"
        log.write(
            f"         EXPECTED:          result_type={expected_output.get('result_type')}  "
            f"command={exp_cmd_name}  landmarks={exp_landmarks}  follow_up={exp_follow_up}\n"
        )
        log.write(
            f"         OBTAINED[Teacher]: result_type={validation['result_type']}  "
            f"command={validation['command_name'] or 'null'}  "
            f"landmarks={validation['landmarks']}  follow_up={act_follow_up}\n"
        )

        if not validation["valid"]:
            console_tqdm_write(
                f"  INVALID {counts} [{use_case}] [{subcategory}] "
                f"\"{input_obj.get('user_message', '')[:50]}\" "
                f"→ {validation['issues']}",
                color="red",
            )

        pbar.set_postfix_str(
            f"{counts} "
            f"avg={sum(stats['inference_times_ms'])/len(stats['inference_times_ms']):.0f}ms"
        )
        if pbar_global is not None:
            if global_counts is not None:
                if validation["valid"]:
                    global_counts["valid"] += 1
                else:
                    global_counts["invalid"] += 1
            pbar_global.update(1)
            if global_counts is not None:
                pbar_global.set_postfix_str(
                    f"ok={global_counts['valid']} bad={global_counts['invalid']} "
                    f"err={global_counts['errors']}  [{category_name}]"
                )

    pbar.close()

    # Write results to output file
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    return stats


def generate_dataset(ollama_url, model, categories, max_examples=None):
    """Generate the dataset by processing each category."""

    console_print("=" * 60)
    console_print("STEP 2: Teacher Dataset Evaluation (8B Teacher)")
    console_print("=" * 60)
    console_print(f"Ollama URL:  {ollama_url}")
    console_print(f"Model:       {model}")
    console_print(f"Dataset dir: {DATASET_DIR}")
    console_print()

    # Load all datasets
    datasets = {}
    total_entries = 0
    for cat_name, cat_key in CATEGORY_KEYS.items():
        if categories and cat_name not in categories:
            continue
        path = get_dataset_path(cat_key)
        entries = load_dataset(path)
        if entries:
            datasets[cat_name] = {"entries": entries, "path": path, "key": cat_key}
            total_entries += len(entries)
            console_print(f"  {cat_name:20s} {len(entries):5d} entries  ({path.name})")

    if not datasets:
        console_print(
            "ERROR: No dataset entries found. Run sintetic_dataset_generator.py first.",
            color="red",
        )
        sys.exit(1)

    console_print(f"\n  {'TOTAL':20s} {total_entries:5d} entries")
    console_print()

    # Apply max_examples limit per category (proportional)
    if max_examples and max_examples < total_entries:
        ratio = max_examples / total_entries
        for cat_name, data in datasets.items():
            n = max(1, int(len(data["entries"]) * ratio))
            random.shuffle(data["entries"])
            data["entries"] = data["entries"][:n]
        new_total = sum(len(d["entries"]) for d in datasets.values())
        console_print(f"  Limited to ~{max_examples} examples ({new_total} actual)\n")

    # Open log file
    log_path = DATASET_DIR / "generation.log"
    all_stats = {}

    total_to_process = sum(len(d["entries"]) for d in datasets.values())
    global_counts = {"valid": 0, "invalid": 0, "errors": 0}
    pbar_global = tqdm(
        total=total_to_process,
        desc="  OVERALL",
        unit="prompt",
        position=0,
        leave=True,
    )

    with open(log_path, "w") as log:
        log.write(f"Dataset generation started at {time.strftime('%Y-%m-%d %H:%M:%S')}\n")
        log.write(f"Ollama URL: {ollama_url} | Model: {model}\n")
        log.write(f"Categories: {list(datasets.keys())}\n")
        log.write(f"Total entries: {total_entries}\n")

        for cat_name, data in datasets.items():
            # Output goes to a results file alongside the prompts file
            output_path = data["path"].parent / data["path"].stem.replace(
                "_prompts", "_results"
            )
            output_path = output_path.with_suffix(".jsonl")

            stats = process_category(
                cat_name, data["entries"], ollama_url, model, output_path, log,
                pbar_global=pbar_global, global_counts=global_counts,
            )
            all_stats[cat_name] = stats

        # Log summary
        log.write(f"\n{'='*80}\n")
        log.write(f"Finished at {time.strftime('%Y-%m-%d %H:%M:%S')}\n")

    pbar_global.close()

    # ---- Final report ----
    console_print()
    console_print("=" * 60)
    console_print("DATASET GENERATION COMPLETE")
    console_print("=" * 60)

    grand_valid = 0
    grand_invalid = 0
    grand_errors = 0
    grand_times = []

    console_print(f"\n  {'Category':20s} {'Total':>6s} {'Valid':>6s} {'Invalid':>8s} {'Errors':>7s} {'Valid%':>7s}")
    console_print(f"  {'-'*20} {'-'*6} {'-'*6} {'-'*8} {'-'*7} {'-'*7}")

    for cat_name, stats in all_stats.items():
        total = stats["total"]
        valid = stats["valid"]
        invalid = stats["invalid"]
        errors = stats["errors"]
        pct = valid / max(total, 1) * 100

        grand_valid += valid
        grand_invalid += invalid
        grand_errors += errors
        grand_times.extend(stats["inference_times_ms"])

        console_print(f"  {cat_name:20s} {total:6d} {valid:6d} {invalid:8d} {errors:7d} {pct:6.1f}%")

    grand_total = grand_valid + grand_invalid + grand_errors
    grand_pct = grand_valid / max(grand_total, 1) * 100
    console_print(f"  {'-'*20} {'-'*6} {'-'*6} {'-'*8} {'-'*7} {'-'*7}")
    console_print(f"  {'TOTAL':20s} {grand_total:6d} {grand_valid:6d} {grand_invalid:8d} {grand_errors:7d} {grand_pct:6.1f}%")

    # Inference time summary
    if grand_times:
        total_sec = sum(grand_times) / 1000
        console_print(f"\n  Inference time:")
        console_print(f"    Min:   {min(grand_times):>8.0f} ms")
        console_print(f"    Max:   {max(grand_times):>8.0f} ms")
        console_print(f"    Avg:   {sum(grand_times)/len(grand_times):>8.0f} ms")
        console_print(f"    Total: {total_sec:>8.1f} sec ({total_sec/60:.1f} min)")

    # Save global stats
    stats_summary = {
        "generated_at": time.strftime("%Y-%m-%d %H:%M:%S"),
        "model": model,
        "total": grand_total,
        "valid": grand_valid,
        "invalid": grand_invalid,
        "errors": grand_errors,
        "valid_pct": round(grand_pct, 1),
        "by_category": {
            cat: {
                "total": s["total"],
                "valid": s["valid"],
                "invalid": s["invalid"],
                "errors": s["errors"],
            }
            for cat, s in all_stats.items()
        },
    }
    if grand_times:
        stats_summary["inference"] = {
            "min_ms": round(min(grand_times), 1),
            "max_ms": round(max(grand_times), 1),
            "avg_ms": round(sum(grand_times) / len(grand_times), 1),
            "total_sec": round(total_sec, 1),
        }

    stats_path = DATASET_DIR / CONFIG.get("paths", {}).get(
        "dataset_stats", "dataset.stats.json"
    )
    with open(stats_path, "w") as f:
        json.dump(stats_summary, f, indent=2)

    console_print(f"\n  Stats: {stats_path}")
    console_print(f"  Log:   {log_path}")
    if CONSOLE_LOG_PATH is not None:
        console_print(f"  Console log: {CONSOLE_LOG_PATH}")
    console_print()

    # Output file paths
    console_print("  Output files:")
    for cat_name, data in datasets.items():
        results_name = data["path"].stem.replace("_prompts", "_results") + ".jsonl"
        console_print(f"    {cat_name:20s} → {data['path'].parent / results_name}")
    console_print()

    return all_stats


