# OLLAMA API CLIENT
# ============================================================

def call_ollama(prompt_json, ollama_url=OLLAMA_URL, model=MODEL_NAME, timeout=None):
    """Send a JSON prompt to the Ollama model and return the response.

    The prompt is the JSON-serialized input object from the dataset.
    The model receives it as the user message and responds with JSON.
    """
    payload = {
        "model": model,
        "stream": False,
        "messages": [{"role": "user", "content": prompt_json}],
    }
    try:
        r = requests.post(ollama_url, json=payload, timeout=timeout or TIMEOUT_SEC)
        r.raise_for_status()
        data = r.json()
        return {
            "content": data["message"]["content"],
            "total_duration_ms": data.get("total_duration", 0) / 1e6,
            "prompt_tokens": data.get("prompt_eval_count", 0),
            "completion_tokens": data.get("eval_count", 0),
        }
    except requests.exceptions.RequestException as e:
        print(f"  ERROR calling Ollama: {e}")
        return None


