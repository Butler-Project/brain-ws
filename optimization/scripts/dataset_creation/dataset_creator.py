def clean_results():
    """Remove ALL generated artifacts: prompts, results, logs, stats, and empty dirs."""
    print("=" * 60)
    print("CLEANING ALL GENERATED ARTIFACTS")
    print("=" * 60)

    removed = []

    # Remove *_prompts.jsonl and *_results.jsonl files
    for cat_key in CATEGORY_KEYS.values():
        prompts_path = get_dataset_path(cat_key)
        results_path = prompts_path.parent / prompts_path.stem.replace(
            "_prompts", "_results"
        )
        results_path = results_path.with_suffix(".jsonl")
        for p in [prompts_path, results_path]:
            if p.exists():
                p.unlink()
                removed.append(p)

    # Remove log and stats
    stats_name = CONFIG.get("paths", {}).get("dataset_stats", "dataset.stats.json")
    for name in ["generation.log", stats_name]:
        p = DATASET_DIR / name
        if p.exists():
            p.unlink()
            removed.append(p)

    # Remove empty subdirectories
    if DATASET_DIR.exists():
        for subdir in sorted(DATASET_DIR.iterdir(), reverse=True):
            if subdir.is_dir() and not any(subdir.iterdir()):
                subdir.rmdir()
                removed.append(subdir)

    print(f"  Removed {len(removed)} files/dirs:")
    for f in removed:
        print(f"    - {f}")
    if not removed:
        print("  Nothing to clean.")
    print()


