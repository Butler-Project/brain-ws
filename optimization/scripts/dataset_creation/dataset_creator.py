def process_category(category_name, entries, ollama_url, model, output_path, log):
    """Process all entries in one category: send to model, validate, write results."""

    stats = {
        "total": len(entries),
        "valid": 0,
        "invalid": 0,
        "errors": 0,
        "inference_times_ms": [],
    }

    results = []

    log.write(f"\n{'='*80}\n")
    log.write(f"CATEGORY: {category_name} ({len(entries)} entries)\n")
    log.write(f"{'='*80}\n\n")

    pbar = tqdm(
        entries,
        desc=f"  {category_name}",
        unit="prompt",
        leave=True,
    )

    for entry in pbar:
        input_obj = entry["input"]
        expected_output = entry["expected_output"]
        use_case = entry.get("use_case", "?")
        subcategory = entry.get("subcategory", "?")

        # Serialize input to JSON string (this is what the model receives)
        prompt_json = json.dumps(input_obj, ensure_ascii=False)

        # Call teacher model
        t_start = time.time()
        response = call_ollama(prompt_json, ollama_url=ollama_url, model=model)
        wall_time_ms = (time.time() - t_start) * 1000

        if response is None:
            stats["errors"] += 1
            log.write(
                f"[ERROR  ] [{use_case:5s}] [{subcategory:35s}] "
                f"wall={wall_time_ms:.0f}ms  "
                f"msg=\"{input_obj.get('user_message', '')[:60]}\"\n"
            )
            results.append({
                **entry,
                "response": None,
                "validation": {"valid": False, "issues": ["Ollama connection error"]},
                "metrics": {"wall_time_ms": wall_time_ms},
            })
            pbar.set_postfix_str(
                f"ok={stats['valid']} bad={stats['invalid']} err={stats['errors']}"
            )
            continue

        # Validate
        validation = validate_response(response["content"], expected_output)

        inference_ms = response["total_duration_ms"]
        stats["inference_times_ms"].append(inference_ms)

        # Build result entry
        result_entry = {
            **entry,
            "response": response["content"],
            "validation": {
                "valid": validation["valid"],
                "is_json": validation["is_json"],
                "result_type": validation["result_type"],
                "command_name": validation["command_name"],
                "landmarks": validation["landmarks"],
                "issues": validation["issues"],
            },
            "metrics": {
                "total_duration_ms": inference_ms,
                "wall_time_ms": wall_time_ms,
                "prompt_tokens": response["prompt_tokens"],
                "completion_tokens": response["completion_tokens"],
            },
        }
        results.append(result_entry)

        # Log
        status = "OK" if validation["valid"] else "INVALID"
        log.write(
            f"[{status:7s}] [{use_case:5s}] [{subcategory:35s}] "
            f"inference={inference_ms:7.0f}ms  wall={wall_time_ms:7.0f}ms  "
            f"tokens={response['prompt_tokens']}+{response['completion_tokens']}  "
            f"msg=\"{input_obj.get('user_message', '')[:60]}\""
        )
        if validation["issues"]:
            log.write(f"\n         ISSUES: {validation['issues']}")
        log.write("\n")

        # Expected vs actual logging
        log.write(
            f"         EXPECTED: result_type={expected_output.get('result_type')}  "
            f"command={expected_output.get('command', {}).get('name') if expected_output.get('command') else 'null'}\n"
        )
        log.write(
            f"         ACTUAL:   result_type={validation['result_type']}  "
            f"command={validation['command_name'] or 'null'}\n"
        )

        if validation["valid"]:
            stats["valid"] += 1
        else:
            stats["invalid"] += 1
            tqdm.write(
                f"  INVALID [{use_case}] [{subcategory}] "
                f"\"{input_obj.get('user_message', '')[:50]}\" "
                f"→ {validation['issues']}"
            )

        pbar.set_postfix_str(
            f"ok={stats['valid']} bad={stats['invalid']} err={stats['errors']} "
            f"avg={sum(stats['inference_times_ms'])/len(stats['inference_times_ms']):.0f}ms"
        )

    pbar.close()

    # Write results to output file
    output_path.parent.mkdir(parents=True, exist_ok=True)
    with open(output_path, "w") as f:
        for r in results:
            f.write(json.dumps(r, ensure_ascii=False) + "\n")

    return stats


def generate_dataset(ollama_url, model, categories, max_examples=None):
    """Generate the dataset by processing each category."""

    print("=" * 60)
    print("STEP 2: Dataset Generation (8B Teacher)")
    print("=" * 60)
    print(f"Ollama URL:  {ollama_url}")
    print(f"Model:       {model}")
    print(f"Dataset dir: {DATASET_DIR}")
    print()

    # Load all datasets
    datasets = {}
    total_entries = 0
    for cat_name, cat_key in CATEGORY_KEYS.items():
        if categories and cat_name not in categories:
            continue
        path = get_dataset_path(cat_key)
        entries = load_dataset(path)
        if entries:
            datasets[cat_name] = {"entries": entries, "path": path, "key": cat_key}
            total_entries += len(entries)
            print(f"  {cat_name:20s} {len(entries):5d} entries  ({path.name})")

    if not datasets:
        print("ERROR: No dataset entries found. Run generate_datasets.py first.")
        sys.exit(1)

    print(f"\n  {'TOTAL':20s} {total_entries:5d} entries")
    print()

    # Apply max_examples limit per category (proportional)
    if max_examples and max_examples < total_entries:
        ratio = max_examples / total_entries
        for cat_name, data in datasets.items():
            n = max(1, int(len(data["entries"]) * ratio))
            random.shuffle(data["entries"])
            data["entries"] = data["entries"][:n]
        new_total = sum(len(d["entries"]) for d in datasets.values())
        print(f"  Limited to ~{max_examples} examples ({new_total} actual)\n")

    # Open log file
    log_path = DATASET_DIR / "generation.log"
    all_stats = {}

    with open(log_path, "w") as log:
        log.write(f"Dataset generation started at {time.strftime('%Y-%m-%d %H:%M:%S')}\n")
        log.write(f"Ollama URL: {ollama_url} | Model: {model}\n")
        log.write(f"Categories: {list(datasets.keys())}\n")
        log.write(f"Total entries: {total_entries}\n")

        for cat_name, data in datasets.items():
            # Output goes to a results file alongside the prompts file
            output_path = data["path"].parent / data["path"].stem.replace(
                "_prompts", "_results"
            )
            output_path = output_path.with_suffix(".jsonl")

            stats = process_category(
                cat_name, data["entries"], ollama_url, model, output_path, log
            )
            all_stats[cat_name] = stats

        # Log summary
        log.write(f"\n{'='*80}\n")
        log.write(f"Finished at {time.strftime('%Y-%m-%d %H:%M:%S')}\n")

    # ---- Final report ----
    print()
    print("=" * 60)
    print("DATASET GENERATION COMPLETE")
    print("=" * 60)

    grand_valid = 0
    grand_invalid = 0
    grand_errors = 0
    grand_times = []

    print(f"\n  {'Category':20s} {'Total':>6s} {'Valid':>6s} {'Invalid':>8s} {'Errors':>7s} {'Valid%':>7s}")
    print(f"  {'-'*20} {'-'*6} {'-'*6} {'-'*8} {'-'*7} {'-'*7}")

    for cat_name, stats in all_stats.items():
        total = stats["total"]
        valid = stats["valid"]
        invalid = stats["invalid"]
        errors = stats["errors"]
        pct = valid / max(total, 1) * 100

        grand_valid += valid
        grand_invalid += invalid
        grand_errors += errors
        grand_times.extend(stats["inference_times_ms"])

        print(f"  {cat_name:20s} {total:6d} {valid:6d} {invalid:8d} {errors:7d} {pct:6.1f}%")

    grand_total = grand_valid + grand_invalid + grand_errors
    grand_pct = grand_valid / max(grand_total, 1) * 100
    print(f"  {'-'*20} {'-'*6} {'-'*6} {'-'*8} {'-'*7} {'-'*7}")
    print(f"  {'TOTAL':20s} {grand_total:6d} {grand_valid:6d} {grand_invalid:8d} {grand_errors:7d} {grand_pct:6.1f}%")

    # Inference time summary
    if grand_times:
        total_sec = sum(grand_times) / 1000
        print(f"\n  Inference time:")
        print(f"    Min:   {min(grand_times):>8.0f} ms")
        print(f"    Max:   {max(grand_times):>8.0f} ms")
        print(f"    Avg:   {sum(grand_times)/len(grand_times):>8.0f} ms")
        print(f"    Total: {total_sec:>8.1f} sec ({total_sec/60:.1f} min)")

    # Save global stats
    stats_summary = {
        "generated_at": time.strftime("%Y-%m-%d %H:%M:%S"),
        "model": model,
        "total": grand_total,
        "valid": grand_valid,
        "invalid": grand_invalid,
        "errors": grand_errors,
        "valid_pct": round(grand_pct, 1),
        "by_category": {
            cat: {
                "total": s["total"],
                "valid": s["valid"],
                "invalid": s["invalid"],
                "errors": s["errors"],
            }
            for cat, s in all_stats.items()
        },
    }
    if grand_times:
        stats_summary["inference"] = {
            "min_ms": round(min(grand_times), 1),
            "max_ms": round(max(grand_times), 1),
            "avg_ms": round(sum(grand_times) / len(grand_times), 1),
            "total_sec": round(total_sec, 1),
        }

    stats_path = DATASET_DIR / CONFIG.get("paths", {}).get(
        "dataset_stats", "dataset.stats.json"
    )
    with open(stats_path, "w") as f:
        json.dump(stats_summary, f, indent=2)

    print(f"\n  Stats: {stats_path}")
    print(f"  Log:   {log_path}")
    print()

    # Output file paths
    print("  Output files:")
    for cat_name, data in datasets.items():
        results_name = data["path"].stem.replace("_prompts", "_results") + ".jsonl"
        print(f"    {cat_name:20s} → {data['path'].parent / results_name}")
    print()

    return all_stats


