def validate_response(response_content, expected_output):
    """Validate the model response against the expected output.

    Checks:
      - Response is valid JSON
      - result_type matches expected
      - command name matches (if expected)
      - landmarks match (if expected)
      - follow_up matches (if expected)
      - Language consistency (no mixing English/Spanish)

    Returns a dict with validation details.
    """
    result = {
        "valid": False,
        "is_json": False,
        "result_type": None,
        "command_name": None,
        "landmarks": [],
        "issues": [],
    }

    # --- Parse JSON ---
    content = response_content.strip()
    try:
        parsed = json.loads(content)
        result["is_json"] = True
    except json.JSONDecodeError:
        result["is_json"] = False
        result["issues"].append("Response is not valid JSON")
        return result

    if not isinstance(parsed, dict):
        result["issues"].append("Response JSON is not a dict")
        return result

    # --- UC-15: Check result_type exists and is valid ---
    VALID_RESULT_TYPES = {
        "model_response",
        "interpreted_implicit_command",
        "interpreted_implicit_command_confirmation",
        "interpreted_explicit_command",
        "pre-emptive_system_command",
        "rejected",
    }

    raw_result_type = parsed.get("result_type")
    if raw_result_type is None:
        result["issues"].append("UC-15: missing result_type — response discarded")
        return result
    if raw_result_type not in VALID_RESULT_TYPES:
        result["issues"].append(
            f"UC-15: invalid result_type '{raw_result_type}' — response discarded"
        )
        return result

    # --- Extract fields ---
    result["result_type"] = parsed.get("result_type")
    cmd = parsed.get("command")
    if cmd and isinstance(cmd, dict):
        result["command_name"] = cmd.get("name")
        params = cmd.get("parameters", {})
        result["landmarks"] = params.get("landmarks_to_visit", [])

    # --- Check version ---
    if parsed.get("version") != "1.0":
        result["issues"].append(
            f"version is '{parsed.get('version')}', expected '1.0'"
        )

    # --- Check result_type ---
    expected_type = expected_output.get("result_type")
    if result["result_type"] != expected_type:
        result["issues"].append(
            f"result_type is '{result['result_type']}', expected '{expected_type}'"
        )

    # --- Check command ---
    expected_cmd = expected_output.get("command")

    if expected_cmd is None:
        # No command expected
        if cmd is not None:
            result["issues"].append(
                f"Got command '{result['command_name']}' but expected null"
            )
    else:
        # Command expected
        if cmd is None:
            result["issues"].append(
                f"Expected command '{expected_cmd['name']}' but got null"
            )
        else:
            # Check command name
            if result["command_name"] != expected_cmd["name"]:
                result["issues"].append(
                    f"command name is '{result['command_name']}', "
                    f"expected '{expected_cmd['name']}'"
                )

            # Check landmarks
            expected_landmarks = expected_cmd.get("parameters", {}).get(
                "landmarks_to_visit"
            )
            if expected_landmarks is not None:
                actual_set = set(result["landmarks"])
                expected_set = set(expected_landmarks)
                if actual_set != expected_set:
                    result["issues"].append(
                        f"landmarks {sorted(actual_set)} != expected {sorted(expected_set)}"
                    )

    # --- Check follow_up ---
    expected_follow_up = expected_output.get("follow_up")
    actual_follow_up = parsed.get("follow_up")

    if expected_follow_up is not None:
        if actual_follow_up is None:
            result["issues"].append("Expected follow_up but got null")
        else:
            if actual_follow_up.get("type") != expected_follow_up.get("type"):
                result["issues"].append(
                    f"follow_up type is '{actual_follow_up.get('type')}', "
                    f"expected '{expected_follow_up.get('type')}'"
                )
    elif expected_follow_up is None and actual_follow_up is not None:
        result["issues"].append("Got follow_up but expected null")

    # --- Check assistant_message exists ---
    if not parsed.get("assistant_message"):
        result["issues"].append("Missing or empty assistant_message")

    # --- Language consistency check ---
    msg = parsed.get("assistant_message", "")
    if msg:
        spanish_markers = ["¿", "¡", "ñ", "á", "é", "í", "ó", "ú"]
        has_spanish = any(c in msg.lower() for c in spanish_markers)
        english_markers = ["would you", "i can", "i'm", "let me", "sure", "hello"]
        has_english = any(m in msg.lower() for m in english_markers)
        if has_spanish and has_english:
            result["issues"].append("Language mixing detected (English + Spanish)")

    # --- Final verdict ---
    if not result["issues"]:
        result["valid"] = True

    return result


