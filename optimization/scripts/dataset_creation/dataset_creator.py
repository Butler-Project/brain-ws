"""
Step 2: Dataset Creator for LLM Optimization Pipeline

Reads the 4 dataset JSONL files (generated by generate_datasets.py),
sends each prompt to the 8B teacher model via Ollama, validates the
response against the expected output, and writes results back.

The communication protocol is JSON→JSON as defined in the ModelFile.

Usage:
    python dataset_creator.py
    python dataset_creator.py --max-examples 50
    python dataset_creator.py --category explicit
    python dataset_creator.py --clean_results

Output: updates each JSONL with model response + validation results
"""

