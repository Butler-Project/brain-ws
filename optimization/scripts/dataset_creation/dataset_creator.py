def main():
    parser = argparse.ArgumentParser(
        description="Generate training dataset using 8B teacher model (JSON→JSON protocol)"
    )
    parser.add_argument(
        "--ollama-url",
        default=OLLAMA_URL,
        help=f"Ollama API URL (default: {OLLAMA_URL})",
    )
    parser.add_argument(
        "--model",
        default=MODEL_NAME,
        help=f"Ollama model name (default: {MODEL_NAME})",
    )
    parser.add_argument(
        "--max-examples", "-n",
        type=int,
        default=CONFIG.get("dataset", {}).get("max_examples"),
        help="Max total examples to process (default: all)",
    )
    parser.add_argument(
        "--category", "-c",
        choices=list(CATEGORY_KEYS.keys()),
        action="append",
        help="Process only specific category (can repeat). Default: all",
    )
    parser.add_argument(
        "--seed",
        type=int,
        default=CONFIG.get("dataset", {}).get("seed", 42),
        help="Random seed (default: 42)",
    )
    parser.add_argument(
        "--clean_results",
        action="store_true",
        help="Remove generated result files and exit",
    )

    args = parser.parse_args()

    if args.clean_results:
        clean_results()
        return

    random.seed(args.seed)

    # Connectivity check
    print("Checking Ollama connectivity...")
    test = call_ollama(
        json.dumps({"version": "1.0", "request_type": "from_user", "user_message": "hello"}),
        ollama_url=args.ollama_url,
        model=args.model,
    )
    if test is None:
        print(f"ERROR: Cannot reach Ollama at {args.ollama_url} with model {args.model}")
        print("Make sure the Ollama Docker container is running.")
        sys.exit(1)
    print(f"OK — Ollama responded in {test['total_duration_ms']:.0f} ms\n")

    generate_dataset(
        ollama_url=args.ollama_url,
        model=args.model,
        categories=args.category,
        max_examples=args.max_examples,
    )


if __name__ == "__main__":
    main()
